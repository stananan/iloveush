// Helper: get all terms for specific units
export function getTermsByUnits(units: number[]): Term[] {
  if (units.length === 0) return TERMS;
  return TERMS.filter(t => t.units.some(u => units.includes(u)));
}

// Helper: pick a random term, optionally filtered by units
export function getRandomTerm(units: number[] = []): Term {
  const pool = getTermsByUnits(units);
  return pool[Math.floor(Math.random() * pool.length)];
}

